#include "fs-util.h"

#include <sys/stat.h>
#include <stdbool.h>

bool fs_is_socket(const char *path)
{
	struct stat st;

	if (stat(path, &st) != 0) {
		return false;
	}

	return S_ISSOCK(st.st_mode);
}
